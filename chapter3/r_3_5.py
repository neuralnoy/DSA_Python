# Explain why the plot of the function n^c is a straight line with slope c on a
# log-log scale.

# because log (n^c) = c * log(n) which is a straight line with the slope c!

# On a log–log plot:
#
# * The x-axis shows ( \log n )
# * The y-axis shows ( \log f(n) )
#
# For the function ( f(n) = n^c ):
#
# \log(n^c) = c\log(n)
#
# This has the form of a linear equation
#
# [
# y = c x
# ]
#
# with:
#
# * ( x = \log n )
# * ( y = \log f(n) )
#
# So the graph is a straight line through the origin with slope ( c ).
